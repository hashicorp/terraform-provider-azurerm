var _ resourceids.ResourceId = OperationId{}

func TestNewOperationID(t *testing.T) {
	id := NewOperationID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceValue", "purgeIdValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.WorkspaceName != "workspaceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'WorkspaceName'", id.WorkspaceName, "workspaceValue")
	}

	if id.PurgeId != "purgeIdValue" {
		t.Fatalf("Expected %q but got %q for Segment 'PurgeId'", id.PurgeId, "purgeIdValue")
	}
}

func TestFormatOperationID(t *testing.T) {
	actual := NewOperationID("12345678-1234-9876-4563-123456789012", "example-resource-group", "workspaceValue", "purgeIdValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.OperationalInsights/workspaces/workspaceValue/operations/purgeIdValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

