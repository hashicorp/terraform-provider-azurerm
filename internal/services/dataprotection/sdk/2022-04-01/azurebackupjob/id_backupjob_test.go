var _ resourceids.ResourceId = BackupJobId{}

func TestNewBackupJobID(t *testing.T) {
	id := NewBackupJobID("12345678-1234-9876-4563-123456789012", "example-resource-group", "vaultValue", "jobIdValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.VaultName != "vaultValue" {
		t.Fatalf("Expected %q but got %q for Segment 'VaultName'", id.VaultName, "vaultValue")
	}

	if id.JobId != "jobIdValue" {
		t.Fatalf("Expected %q but got %q for Segment 'JobId'", id.JobId, "jobIdValue")
	}
}

func TestFormatBackupJobID(t *testing.T) {
	actual := NewBackupJobID("12345678-1234-9876-4563-123456789012", "example-resource-group", "vaultValue", "jobIdValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.DataProtection/backupVaults/vaultValue/backupJobs/jobIdValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

