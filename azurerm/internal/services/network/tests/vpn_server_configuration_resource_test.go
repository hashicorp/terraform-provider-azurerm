func (t VPNServerConfigurationResource) Exists(ctx context.Context, clients *clients.Client, state *terraform.InstanceState) (*bool, error) {
	id, err := network.ParseVpnServerConfigurationID(state.ID)
	if err != nil {
		return nil, err
	}

	resp, err := clients.Network.VpnServerConfigurationsClient.Get(ctx, id.ResourceGroup, id.Name)
	if err != nil {
		return nil, fmt.Errorf("reading Vpn Server Configuration (%s): %+v", id, err)
	}

	return utils.Bool(resp.ID != nil), nil
}

